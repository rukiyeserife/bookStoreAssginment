DROP TABLE IF EXISTS TBL_CUSTOMER;
DROP TABLE IF EXISTS TBL_ORDER;
DROP TABLE IF EXISTS TBL_ORDER_DETAIL;
DROP TABLE IF EXISTS TBL_BOOK;
DROP TABLE IF EXISTS TBL_ORDER_BOOK;

CREATE TABLE TBL_CUSTOMER (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  first_name VARCHAR(250) NOT NULL,
  last_name VARCHAR(250) NOT NULL,
  adress VARCHAR(500) NOT NULL,
  phone VARCHAR(30) NOT NULL,
  email VARCHAR(250) DEFAULT NULL
);

CREATE TABLE TBL_BOOK (
  id Long AUTO_INCREMENT  PRIMARY KEY,
  bookName VARCHAR(250) NOT NULL,
  bookVendor VARCHAR(250) NOT NULL,
  bookDescription VARCHAR(250) NOT NULL,
  quantityInStock INT NOT NULL,
  buyPrice DOUBLE NOT NULL
);

CREATE TABLE TBL_ORDER (
  id INT AUTO_INCREMENT  PRIMARY KEY,
  orderDate DATE NOT NULL,
  shippedDate DATE NOT NULL,
  status BOOLEAN,
  comments VARCHAR(250) DEFAULT NULL,
  customerNoFK INT,
  FOREIGN KEY(customerNoFK) REFERENCES TBL_CUSTOMER,
  quantityInOrdered INT NOT NULL
);

CREATE TABLE TBL_ORDER_BOOK (
  bookIdFK INT,
  FOREIGN KEY(bookIdFK) REFERENCES TBL_BOOK,
  orderIdFK INT,
  FOREIGN KEY(orderIdFK) REFERENCES TBL_ORDER

);






